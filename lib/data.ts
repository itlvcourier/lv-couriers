// Helper to generate timestamps
const hoursAgo = (hours: number): string => {
  const date = new Date()
  date.setHours(date.getHours() - hours)
  return date.toISOString()
}

const minsAgo = (mins: number): string => {
  const date = new Date()
  date.setMinutes(date.getMinutes() - mins)
  return date.toISOString()
}

